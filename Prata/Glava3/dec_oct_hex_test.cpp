#include<iostream>

using namespace std ;

int main(){
    int a = 43 ;
    int b = 43 ;
    int c = 43 ;

    cout << "a = " << a << endl ;
    cout << oct ;
    cout << "b = " << b << endl ;
    cout << hex ;
    cout << "c = " << c << endl ;
    cout << dec ;

    a = 43 ;
    b = 043 ;
    c = 0x43 ;
    cout << "a = " << a << endl ;
    cout << "b = " << b << endl ;
    cout << "c = " << c << endl ;

    return 0 ;
}